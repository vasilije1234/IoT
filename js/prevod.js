function prevedi(){

    for(var key in labelePrevod) {
        if (labelePrevod[key].tip == "tekst"){
            if (jezik == 'mne')
                $("#" + key).text(labelePrevod[key].mne);
            if (jezik == 'eng')
                $("#" + key).text(labelePrevod[key].eng);
        }
        
        if (labelePrevod[key].tip == "placeholder"){
            if (jezik == 'mne')
                $("#" + key).attr("placeholder", labelePrevod[key].mne);
            if (jezik == 'eng')
                $("#" + key).attr("placeholder", labelePrevod[key].eng);
        }
        if (labelePrevod[key].tip == "data-filter-placeholder"){
            if (jezik == 'mne')
                $("#" + key).attr("data-filter-placeholder", labelePrevod[key].mne);
            if (jezik == 'eng')
                $("#" + key).attr("data-filter-placeholder", labelePrevod[key].eng);
            
        }
    }
    
    for(var key in porukeKorisniku) {
        if (jezik == 'mne')
            porukaOdabraniJezik[key] = porukeKorisniku[key].mne;
        if (jezik == 'eng')
            porukaOdabraniJezik[key] = porukeKorisniku[key].eng;
    }
}